operacao = input("Digite a operação matematica desejada: ")
num1 = float(input("Digite um valor: "))
num2 = float(input("Digite um valor: "))

if operacao == "+":
    print(num1 + num2)

elif operacao == "-":
    print(num1 - num2)

elif operacao == "/":
    print(num1 / num2)

elif operacao == "*":
    print(num1 * num2)